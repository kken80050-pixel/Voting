    private void loadvoters(){
        model.setRowCount(0);
        
        try(BufferedReader br = new BufferedReader(new FileReader("notvoted.txt"))){
            String line;
            while ((line = br.readLine()) != null){
                String[] data = line.split(",");
                if (data.length == 4){
                    model.addRow(new Object[]{data[0], data[1], data[2], data[3]});
                }
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }

