    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        
        if (row == -1){
            JOptionPane.showMessageDialog(this, "Please select a voter to remove.");
            return;
        }
        model.removeRow(row);
        
        savevoters();
        
        JOptionPane.showMessageDialog(this, "Voter removed succesfully.");
    }//GEN-LAST:event_jButton2ActionPerformed
    private void savevoters(){
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("notvoted.txt"))){
            for (int i = 0; i < model.getRowCount(); i++){
                String username = model.getValueAt(i, 0).toString();
                String Voterid = model.getValueAt(i, 1).toString();
                 String Age = model.getValueAt(i, 2).toString();
                  String Address = model.getValueAt(i, 3).toString();
                  
                  bw.write(username + "," + Voterid + "," + Age + "," + Address);
                  bw.newLine();
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }
