    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText().trim();
        String age = jTextField2.getText().trim();
        String address = jTextField3.getText().trim();
        String votersid = jTextField4.getText().trim();
        
        if(name.isEmpty() || age.isEmpty() || address.isEmpty()){
            JOptionPane.showMessageDialog(this, "All Fields Required");
            return;
        }
        int intage;
        try {
            intage = Integer.parseInt(age);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Age Must be Valid", "Invalid Age", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (intage < 18 ){
            JOptionPane.showMessageDialog(this, "Only Voters Age 18 or Above are able to Register.", "Age Restriction", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (voteridexist(votersid)){
            JOptionPane.showMessageDialog(this, "Voters ID already Exists.", "Duplicate Voters ID", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("notvoted.txt", true))){
            bw.write(name + "," + votersid + "," + age + "," + address);
            bw.newLine();
            JOptionPane.showMessageDialog(this, "Voter Registered");
             dispose();
        } catch (Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

