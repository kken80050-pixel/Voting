    private Boolean voteridexist(String voterid){
        try (BufferedReader br = new BufferedReader(new FileReader("notvoted.txt"))){
            String line;
            while ((line = br.readLine()) != null){
                String[] data = line.split(",");
                if (data.length >= 4 && data[1].equals(voterid)){
                    return true;
                }
            }
        } catch (Exception e){
            
        }
        return false;
    }

